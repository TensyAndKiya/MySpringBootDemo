package com.clei.bo;

/**
 * es range
 *
 * @author KIyA
 * @date 2021-01-25
 */
public class ESRange {

    /**
     * from
     */
    private Double from;

    /**
     * to
     */
    private Double to;

    /**
     * range key
     */
    private String key;

    public Double getFrom() {
        return from;
    }

    public void setFrom(Double from) {
        this.from = from;
    }

    public Double getTo() {
        return to;
    }

    public void setTo(Double to) {
        this.to = to;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
